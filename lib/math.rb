def addition(num1, num2)
  add = num1 + num2
  return add
end

def subtraction(num1, num2)
  subtract = num2 - num1
  return subtract
end

def division(num1, num2)
  divide = num2 / num1
  return divide
end

def multiplication(num1, num2)
  multiply = num1 * num2
  return multiply
end

def modulo(num1, num2)
  remainder = num2 % num1
  return remainder
end

def square_root(num)
  squared = num**
  return squared
end

